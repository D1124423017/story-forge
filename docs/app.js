const promptKeys = {
  launch: "prompt1Body",
  chapter: "prompt2Body",
  stuck: "prompt3Body",
  revision: "prompt4Body",
};

const state = {
  lang: localStorage.getItem("storyForgeLang") || "zh-Hant",
};

function applyLanguage(lang) {
  const table = dictionary[lang] || dictionary["zh-Hant"];
  state.lang = lang;
  localStorage.setItem("storyForgeLang", lang);
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (table[key]) {
      node.textContent = table[key];
    }
  });

  document.querySelectorAll(".lang-button").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });
}

document.querySelectorAll(".lang-button").forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

document.querySelectorAll("[data-copy]").forEach((button) => {
  button.addEventListener("click", async () => {
    const key = promptKeys[button.dataset.copy];
    const table = dictionary[state.lang] || dictionary["zh-Hant"];
    const status = document.querySelector(".copy-status");
    try {
      await navigator.clipboard.writeText(table[key]);
      status.textContent = table.copied;
    } catch {
      status.textContent = table[key];
    }
  });
});

applyLanguage(state.lang);
